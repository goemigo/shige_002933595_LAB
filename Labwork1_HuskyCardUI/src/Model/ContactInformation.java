package Model;

/**
 *
 * @author emi
 */
public class ContactInformation {
    String phone;
    String email;
    
    public ContactInformation(){
        this.phone = "";
        this.email = "";
    }
    
    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    

}
